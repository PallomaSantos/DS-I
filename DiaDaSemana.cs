using System;

class Program
{
    static void Main() {
        Console.WriteLine("Digite um dia da semana para converter por extenso: ");
        int DiaSemana = Convert.ToInt32(Console.ReadLine());

        switch (DiaSemana)
        {
            case 1:
                Console.WriteLine("Domingo");
                break;
            case 2:
                Console.WriteLine("Segunda");
                break;
            case 3:
                Console.WriteLine("Terça");
                break;
            case 4:
                Console.WriteLine("Quarta");
                break;  
            case 5:
                Console.WriteLine("Quinta");
                break;  
            case 6:
                Console.WriteLine("Sexta");
                break;
            case 7:
                Console.WriteLine("Sábado");
                break;  
            default: 
                Console.WriteLine("Que semana é essa?");
                break;

        }   
    
    
    }
}
